export function getTranslation(lang: Language, key: string, fallbackText?: string): string {
  // Check main TRANSLATIONS dictionary
  const mainDict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  if (mainDict && mainDict[key] !== undefined) {
    return mainDict[key];
  }
  
  // Check TELEMETRY_DICTIONARY dictionary
  const telDict = TELEMETRY_DICTIONARY[lang] || TELEMETRY_DICTIONARY.en;
  if (telDict && telDict[key] !== undefined) {
    return telDict[key];
  }

  // Check BILLING_DICTIONARY dictionary
  const billDict = BILLING_DICTIONARY[lang] || BILLING_DICTIONARY.en;
  if (billDict && billDict[key] !== undefined) {
    return billDict[key];
  }

  // Check ADDITIONAL_TRANSLATIONS dictionary
  const addDict = ADDITIONAL_TRANSLATIONS[lang] || ADDITIONAL_TRANSLATIONS.en;
  if (addDict && addDict[key] !== undefined) {
    return addDict[key];
  }
  
  // Try English fallback of primary dictionary
  const mainEn = TRANSLATIONS.en[key];
  if (mainEn !== undefined) {
    return mainEn;
  }
  
  // Try English fallback of additional dictionary
  const addEn = ADDITIONAL_TRANSLATIONS.en?.[key];
  if (addEn !== undefined) {
    return addEn;
  }
  
  return fallbackText || key;
}

